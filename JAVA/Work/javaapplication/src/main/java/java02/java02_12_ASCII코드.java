package java02;

public class java02_12_ASCII코드 {
    public static void main(String[] args){
        int x ='4';
        int y ='5';
        int result = 0;
        
        result = x+y;
        System.out.println("더하기는:"+result); //출력: 더하기 =9
        
        result = x-y;
        System.out.println("빼기는:"+result); //출력: 더하기 =9
        
        
        result = x*y;
        System.out.println("곱하기는:"+result); //출력: 더하기 =9
        
        result = x/y;
        System.out.println("나누기는:"+result); //출력: 더하기 =9
        
        result = x%y;
        System.out.println("나머지는:"+result); //출력: 더하기 =9
    }
    
    
    
}
